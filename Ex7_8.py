def converter_caixa_alta(texto):
    return texto.upper()

# Exemplos de uso
string1 = "Olá, Mundo!"
string2 = "Python é uma linguagem de programação poderosa."
string3 = "Este é um exemplo de STRING com letras mistas."

# Convertendo as strings para caixa alta
resultado1 = converter_caixa_alta(string1)
resultado2 = converter_caixa_alta(string2)
resultado3 = converter_caixa_alta(string3)

# Exibindo os resultados
print("Resultado 1:", resultado1)
print("Resultado 2:", resultado2)
print("Resultado 3:", resultado3)
